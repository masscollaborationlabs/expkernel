typedef unsigned char u8;

static volatile u8* const FB = (u8*)0xA0000;
static const int WIDTH = 320;
static const int HEIGHT = 200;

static inline void outb(unsigned short port, unsigned char value) {
    __asm__ volatile ("outb %0, %1" : : "a"(value), "Nd"(port));
}

static int str_len(const char* s) {
    int n = 0;
    while (s[n] != '\0') {
        ++n;
    }
    return n;
}

static void put_pixel(int x, int y, u8 color) {
    if (x < 0 || x >= WIDTH || y < 0 || y >= HEIGHT) {
        return;
    }
    FB[y * WIDTH + x] = color;
}

static void fill_screen(u8 color) {
    for (int i = 0; i < WIDTH * HEIGHT; ++i) {
        FB[i] = color;
    }
}

static void draw_circle_outline(int cx, int cy, int r, int thickness, u8 color) {
    const int outer = r * r;
    const int inner_r = (r - thickness) > 0 ? (r - thickness) : 0;
    const int inner = inner_r * inner_r;

    for (int y = -r; y <= r; ++y) {
        for (int x = -r; x <= r; ++x) {
            const int d = x * x + y * y;
            if (d <= outer && d >= inner) {
                put_pixel(cx + x, cy + y, color);
            }
        }
    }
}

